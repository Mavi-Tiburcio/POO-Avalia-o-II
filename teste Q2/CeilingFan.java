public class CeilingFan {
    public void high() {
        System.out.println("Ventilador de teto está em ALTO");
    }

    public void medium() {
        System.out.println("Ventilador de teto está em MÉDIO");
    }

    public void low() {
        System.out.println("Ventilador de teto está em BAIXO");
    }

    public void off() {
        System.out.println("Ventilador de teto DESLIGADO");
    }

    public void getSpeed() {
        System.out.println("A velocidade do ventilador de teto está aumentando");
    }
}
